function norm(s) {
  return s.toLowerCase()
    .normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9\s]/g, '')
    .trim();
}

const lookup = new Map();
DATOS.forEach(d => {
  const key = norm(d.n) + '|' + norm(d.d);
  lookup.set(key, d);
});

