let updated = 0;
const notFound = [];

function muniCatFromPop(pop) {
  if (pop > 100000) return 'A';
  if (pop >= 20000) return 'B';
  if (pop >= 5000)  return 'C';
  return 'D';
}

MUNICIPIOS.forEach(muni => {
  const key = norm(muni.nombre) + '|' + norm(muni.departamento);
  const aliasKey = ALIASES[key];
  let dato = lookup.get(aliasKey || key);

  if (!dato) {
    // fallback: match solo por nombre
    const keyNombre = norm(muni.nombre);
    for (const [k, v] of lookup) {
      if (k.startsWith(keyNombre + '|')) { dato = v; break; }
    }
  }

  if (dato) {
    muni.categoria = dato.c;
    if (Array.isArray(muni.evolucion)) {
      muni.evolucion.forEach(evo => {
        if (dato.p[evo.year]) {
          evo.poblacion = dato.p[evo.year];
        } else if (evo.year === 2019 && dato.p[2020]) {
          evo.poblacion = Math.round(dato.p[2020] * 0.975);
        }
      });
    }
    if (dato.p[2024]) muni.poblacion = dato.p[2024];
    updated++;
  } else {
    // Sin datos oficiales: derivar categoría desde poblacion existente
    notFound.push(`${muni.nombre} (${muni.departamento}) → cat ${muniCatFromPop(muni.poblacion)} por poblacion ${muni.poblacion}`);
    muni.categoria = muniCatFromPop(muni.poblacion);
  }
});

